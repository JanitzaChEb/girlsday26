void setzeLEDFarbe(LED_Farbe farbe)
{
  if (farbe == ROT)
  {
    digitalWrite(LED_ROT, HIGH);
    digitalWrite(LED_GELB, LOW);
    digitalWrite(LED_GRUEN, LOW);
  }
  if (farbe == GELB)
  {
    digitalWrite(LED_ROT, LOW);
    digitalWrite(LED_GRUEN, LOW);
    digitalWrite(LED_GELB, HIGH);
  }
  if (farbe == AUS)
  {
    digitalWrite(LED_ROT, LOW);
    digitalWrite(LED_GELB, LOW);
    digitalWrite(LED_GRUEN, LOW);
  }
  if (farbe == GRUEN)
  {
    digitalWrite(LED_ROT, LOW);
    digitalWrite(LED_GELB, LOW);
    digitalWrite(LED_GRUEN, HIGH);
  }
}

int rechneMesswertInProzentUm(int raw, int messwert_min, int messwert_max)
{
  raw = constrain(raw, messwert_min, messwert_max);
  return map(raw, messwert_min, messwert_max, 0, 100);
}

int leseFeuchtigkeitswertInProzent(int messwert_trocken, int messwert_nass)
{
  digitalWrite(SENSOR_STROMVERSORGUNG, HIGH);
  delay(10);
  messwert_feuchtigkeit = analogRead(SENSOR);
  digitalWrite(SENSOR_STROMVERSORGUNG, LOW);
  prozent_feuchtigkeit = rechneMesswertInProzentUm(messwert_trocken-messwert_feuchtigkeit, messwert_nass, messwert_trocken);
  return prozent_feuchtigkeit;
}

int leseLichtInProzent(int messwert_dunkel, int messwert_hell)
{
  messwert_licht = analogRead(LDR);
  prozent_licht = rechneMesswertInProzentUm(messwert_licht, messwert_dunkel, messwert_hell);
  return prozent_licht;
}

void aktiviereFeuchtigkeitswertAufDisplay()
{
  zeigeFeuchtigkeit = true;
}

void setzeEmotion(Emotion neueEmotion)
{
  if (emotion != neueEmotion)
  {
    emotion = neueEmotion;
    g_frameCounter = 0;
    g_lastFrameSwitchMs = millis();
  }
}

void spieleAnimation(Emotion emotion)
{
  const Animation *anim = animationFuerEmotion(emotion);
  const int x = (SCREEN_WIDTH - anim->width) / 2;
  const int y = (SCREEN_HEIGHT - anim->height) / 2;
  
  for (uint8_t i = 0; i < anim->frameCount; i++)
    {
        const uint8_t* frame = anim->frames[i];

        display.clearDisplay();
        display.drawBitmap(x, y, frame, anim->width, anim->height, SSD1306_WHITE);
        display.display();

        delay(anim->frameDelayMs);
    }
}

void aktualisiereDisplay()
{
  display.clearDisplay();

  const Animation *anim = animationFuerEmotion(emotion);

  // Frame-Logik: alle frameDelayMs den Frame weiterschalten
  const uint32_t now = millis();
  if ((uint32_t)(now - g_lastFrameSwitchMs) >= anim->frameDelayMs)
  {
    g_lastFrameSwitchMs = now;
    g_frameCounter++;
  }

  const uint8_t frameIndex = (anim->frameCount == 0) ? 0 : (g_frameCounter % anim->frameCount);
  const uint8_t *frame = anim->frames[frameIndex];

  const int x = (SCREEN_WIDTH - anim->width) / 2;
  const int y = (SCREEN_HEIGHT - anim->height) / 2;
  display.drawBitmap(x, y, frame, anim->width, anim->height, SSD1306_WHITE);

  if (zeigeFeuchtigkeit)
  {
    display.setTextSize(1);
    display.setTextColor(WHITE);
    display.setCursor(100, 0);
    display.println(messwert_feuchtigkeit);
    display.setCursor(100, 9);
    display.println(String(prozent_feuchtigkeit) + "%");

    display.setCursor(0, 0);
    display.println(messwert_licht);
    display.setCursor(0, 9);
    display.println(String(prozent_licht) + "%");
  }

  display.display();
}