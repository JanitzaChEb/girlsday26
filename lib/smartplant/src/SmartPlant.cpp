static const uint8_t *const frames_laugh[] = {bmp_laugh_0, bmp_laugh_1};
static const uint8_t *const frames_sleep[] = {bmp_sleep_0, bmp_sleep_1};

static const Animation anim_neutral = {frames_neutral, 2, 250, 68, 62};
static const Animation anim_sad = {frames_sad, 2, 250, 68, 62};
static const Animation anim_happy = {frames_happy, 2, 200, 68, 62};
static const Animation anim_laugh = {frames_laugh, 2, 150, 68, 62};
static const Animation anim_sleep = {frames_sleep, 2, 250, 68, 62};

static const Animation *animationFuerEmotion(Emotion e)
{
  switch (e)
  {
  case TRAURIG:
    return &anim_sad;
  case GLUECKLICH:
    return &anim_happy;
  case LACHT:
    return &anim_laugh;
  case SCHLAEFT:
    return &anim_sleep;
  case NEUTRAL:
  default:
    return &anim_neutral;
  }
}

